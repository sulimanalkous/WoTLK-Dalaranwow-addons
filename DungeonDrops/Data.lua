    if universalSlots[slot] then return true end
    if slot == "Off-Hand" then
        return class ~= "MAGE" and class ~= "WARLOCK" and class ~= "HUNTER" and class ~= "ROGUE" and class ~= "DRUID"
    end
    if slot == "Shield" then
        return class == "WARRIOR" or class == "PALADIN" or class == "SHAMAN"
