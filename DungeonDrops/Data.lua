function DungeonDropsData:GetDungeonsForLevel(level)
    local results = {}
    for key, dungeon in pairs(self.Dungeons) do
        if level >= dungeon.minLevel and level <= dungeon.maxLevel then
            results[key] = dungeon
        end
    end
    return results
end

function DungeonDropsData:GetAllDungeons()
    local list = {}
    for key, dungeon in pairs(self.Dungeons) do
        table.insert(list, { key = key, name = dungeon.name, minLevel = dungeon.minLevel, maxLevel = dungeon.maxLevel, expansion = dungeon.expansion })
    end
    table.sort(list, function(a, b) return a.minLevel < b.minLevel end)
    return list
end

function DungeonDropsData:GetItemsForLevel(level, playerClass)
    local results = {}
    for dKey, dungeon in pairs(self.Dungeons) do
        if level >= dungeon.minLevel and level <= dungeon.maxLevel then
            if self.ItemCache then
                for _, itemID in ipairs(self.ItemCache[dKey] or {}) do
                    table.insert(results, {
                        dungeonKey = dKey,
                        dungeonName = dungeon.name,
                        dungeonZone = dungeon.zone,
                        dungeonExpansion = dungeon.expansion,
                        itemID = itemID,
                    })
                end
            end
        end
    end
    return results
end

-- Build item->dungeon mapping from AtlasLoot data at runtime
function DungeonDropsData:InitFromAtlasLoot()
    self.ItemCache = {}
    if not AtlasLootNewBossButtons then return end

    -- Force-load AtlasLoot expansion modules (LoadOnDemand)
    LoadAddOn("AtlasLoot_OriginalWoW")
    LoadAddOn("AtlasLoot_BurningCrusade")
    LoadAddOn("AtlasLoot_WrathoftheLichKing")
    if not AtlasLoot_Data then return end
    for dKey, dungeon in pairs(self.Dungeons) do
        local atlasKey = dungeon.atlasKey
        if atlasKey then
            local bossKeys = AtlasLootNewBossButtons[atlasKey]
            if bossKeys then
                local seen = {}
                self.ItemCache[dKey] = {}
                for _, bossKey in ipairs(bossKeys) do
                    if bossKey and bossKey ~= "" then
                        local lootTable = AtlasLoot_Data and AtlasLoot_Data[bossKey]
                        if lootTable then
                            for _, entry in ipairs(lootTable) do
                                local id = entry[2]
                                if type(id) == "number" and id > 0 and not seen[id] then
                                    seen[id] = true
                                    tinsert(self.ItemCache[dKey], id)
                                end
                            end
                        end
                    end
                end
            end
        end
    end
end

function DungeonDropsData:EnrichFromAtlasLoot()
    if not AtlasLoot_Data then return end
    self.ALIconCache = {}
    for _, lootTable in pairs(AtlasLoot_Data) do
        if type(lootTable) == "table" and #lootTable > 0 then
            for _, entry in ipairs(lootTable) do
                if type(entry[2]) == "number" and entry[2] > 0 then
                    local iconRef = entry[3]
                    if type(iconRef) == "string" and iconRef ~= "" and iconRef ~= "?" then
                        self.ALIconCache[entry[2]] = "Interface\\Icons\\" .. iconRef
                    end
                end
            end
        end
    end
end

function DungeonDropsData:LookupAtlasLootIcon(itemID)
    if self.ALIconCache and self.ALIconCache[itemID] then
        return self.ALIconCache[itemID]
    end
    if not AtlasLoot_Data then return nil end
    for _, lootTable in pairs(AtlasLoot_Data) do
        if type(lootTable) == "table" and #lootTable > 0 then
            for _, entry in ipairs(lootTable) do
                if entry[2] == itemID then
                    local iconRef = entry[3]
                    if type(iconRef) == "string" and iconRef ~= "" and iconRef ~= "?" then
                        local path = "Interface\\Icons\\" .. iconRef
                        if not self.ALIconCache then self.ALIconCache = {} end
                        self.ALIconCache[itemID] = path
                        return path
                    end
                    return nil
                end
            end
        end
    end
    return nil
end