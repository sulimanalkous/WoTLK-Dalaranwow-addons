    local classWeapons = {
        WARRIOR    = { ["One-Hand Sword"]=true,["One-Hand Mace"]=true,["One-Hand Axe"]=true,["Two-Hand Sword"]=true,["Two-Hand Mace"]=true,["Two-Hand Axe"]=true,Dagger=true,Polearm=true,Bow=true,Gun=true,Crossbow=true,Ranged=true,Fist=true },
